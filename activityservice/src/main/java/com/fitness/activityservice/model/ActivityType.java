package com.fitness.activityservice.model;

public enum ActivityType {
    /**
     * 跑步 - 户外或跑步机上的跑步运动
     */
    RUNNING,

    /**
     * 骑行 - 自行车骑行运动，包括室内动感单车
     */
    CYCLING,

    /**
     * 游泳 - 各种泳姿的游泳活动
     */
    SWIMMING,

    /**
     * 重量训练 - 使用器械或自由重量进行的力量训练
     */
    WEIGHT_TRAINING,

    /**
     * 瑜伽 - 包含各种流派的瑜伽练习
     */
    YOGA,

    /**
     * 普拉提 - 注重核心力量和控制的身体训练方法
     */
    PILATES,

    /**
     * 尊巴 - 结合拉丁舞蹈和有氧运动的健身课程
     */
    ZUMBA,

    /**
     * 舞蹈 - 各种形式的舞蹈活动
     */
    DANCING,

    /**
     * 高强度间歇训练 - 交替进行高强度运动和短暂休息的训练方式
     */
    HIIT,

    /**
     * 力量训练 - 增强肌肉力量和耐力的训练
     */
    STRENGTH_TRAINING,

    /**
     * 有氧运动 - 提高心肺功能的持续性运动
     */
    CARDIO,

    /**
     * 拉伸 - 提高柔韧性的伸展运动
     */
    STRETCHING,

    /**
     * 冥想 - 专注和放松的心理练习
     */
    MEDITATION,

    /**
     * 体操 - 包含各种体操动作的训练
     */
    GYMNASTICS,

    /**
     * 空手道 - 日本传统武术
     */
    KARATE,

    /**
     * 拳击 - 使用拳击技术的训练或比赛
     */
    BOXING,

    /**
     * 太极 - 中国传统武术，强调缓慢流畅的动作
     */
    TAI_CHI,

    /**
     * 摔跤 - 近身格斗运动
     */
    WRESTLING
}
